    private void carregarVeiculo() {
        VeiculoDAO dao = new VeiculoDAO();
        veiculo = dao.getVeiculoPorId(veiculoId);
        if (veiculo != null) {
           
            txtPlacaVeiculo.setText(veiculo.getPlaca());
            txtPlacaVeiculo.setEditable(false);
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao carregar veículo.");
            dispose();
        }
    }
    
    private void gerarArquivoLocacao(String nome, String cpf, String telefone, String dataLocacao, String dataDevolucao) {
    try {
        File desktopDir = FileSystemView.getFileSystemView().getHomeDirectory();
        File file = new File(desktopDir, "locacao_" + veiculo.getId() + ".txt");

        // Escreve os dados no arquivo
        try (BufferedWriter writer = new BufferedWriter(new FileWriter(file))) {
            writer.write("Locação de Veículo\n");
            writer.write("-----------------------------\n");
            writer.write("Nome: " + nome + "\n");
            writer.write("CPF: " + cpf + "\n");
            writer.write("Telefone: " + telefone + "\n");
            writer.write("Veículo: " + veiculo.getModelo() + " - " + veiculo.getFabricante() + "\n");
            writer.write("Placa: " + veiculo.getPlaca() + "\n");
            writer.write("Data da Locação: " + dataLocacao + "\n");
            writer.write("Data da Devolução: " + dataDevolucao + "\n");
            writer.write("-----------------------------\n");
        }

        JOptionPane.showMessageDialog(this, "Arquivo de locação salvo com sucesso na área de trabalho!");

    } catch (IOException e) {
        e.printStackTrace();
        JOptionPane.showMessageDialog(this, "Erro ao salvar arquivo de locação: " + e.getMessage());
    }
}


