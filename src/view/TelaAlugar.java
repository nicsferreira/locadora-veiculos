    private void btnAlugarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlugarActionPerformed
        if (txtNomeCliente.getText().isEmpty() || txtCpfCliente.getText().isEmpty() 
            || txtTelefone.getText().isEmpty() || txtDataLocacao.getText().isEmpty() 
            || txtDataDevolucao.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Preencha todos os campos antes de alugar.");
            return;
        }    
        String nome = txtNomeCliente.getText();
        String cpf = txtCpfCliente.getText();
        if (!cpf.matches("\\d{3}\\.\\d{3}\\.\\d{3}-\\d{2}")) {
            JOptionPane.showMessageDialog(this, "CPF inválido. Formato correto: 000.000.000-00");
            return;
        }
        String telefone = txtTelefone.getText();
        if (!telefone.matches("\\(\\d{2}\\)\\s\\d{4,5}-\\d{4}")) {
            JOptionPane.showMessageDialog(this, "Telefone inválido. Formato correto: (00) 91234-5678");
            return;
        }
        String dataLocacao = txtDataLocacao.getText();
        if (!dataLocacao.matches("\\d{2}/\\d{2}/\\d{4}")) {
            JOptionPane.showMessageDialog(this, "Data de locação inválida. Formato correto: dd/mm/aaaa");
            return;
        }
        String dataDevolucao = txtDataDevolucao.getText();
        if (!dataDevolucao.matches("\\d{2}/\\d{2}/\\d{4}")) {
            JOptionPane.showMessageDialog(this, "Data de devolução inválida. Formato correto: dd/mm/aaaa");
            return;
        }

        VeiculoDAO dao = new VeiculoDAO();
        veiculo.setDisponivel(false); 
        dao.atualizarVeiculo(veiculo);

        gerarArquivoLocacao(nome, cpf, telefone, dataLocacao, dataDevolucao);

        JOptionPane.showMessageDialog(this, "Locação registrada com sucesso!");

        telaControle.carregarTabelaVeiculos();
        dispose();
    }//GEN-LAST:event_btnAlugarActionPerformed

